package conditionallooping;

public class eg3 {
    public static void main(String[] args) {

        for (int i = 1; i <=5 ; i++)   // outer loop i = 1 2
        {
            for (int j = 1; j <=5 ; j++) //inner loop j = 1 2 3 4 5
            {
                System.out.print("* ");
            }

            System.out.println();

        }


    }
}
